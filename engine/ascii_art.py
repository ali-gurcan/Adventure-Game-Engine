def get_item_icon(name: str) -> str:
    """Returns a single emoji icon for an item based on its name."""
    name_lower = name.lower()
    for keyword, icon in ITEM_ICONS.items():
        if keyword in name_lower:
            return icon
    return '📦 '  # generic box


# ─────────────────────────────────────────
# LOOKUP FUNCTIONS
# ─────────────────────────────────────────

def get_room_art(name: str) -> str:
    """Returns ASCII art for a room based on its name keywords."""
    n = name.lower()
    if any(k in n for k in ['village', 'town', 'hamlet', 'settlement', 'square']):
        return ART_VILLAGE
    if any(k in n for k in ['market', 'bazaar', 'shop', 'district', 'trade']):
        return ART_MARKET
    if any(k in n for k in ['forest', 'wood', 'glade', 'grove', 'jungle']):
        return ART_FOREST
    if any(k in n for k in ['swamp', 'marsh', 'bog', 'fen', 'mire', 'cursed']):
        return ART_SWAMP
    if any(k in n for k in ['tower', 'wizard', 'spire', 'mage', 'arcane']):
        return ART_TOWER
    if any(k in n for k in ['castle', 'keep', 'fort', 'citadel', 'stronghold']):
        return ART_CASTLE
    if any(k in n for k in ['throne', 'throne room', 'hall', 'palace', 'royal']):
        return ART_THRONE
    if any(k in n for k in ['cave', 'mine', 'pit', 'lair', 'grotto', 'crystal']):
        return ART_CAVE
    if any(k in n for k in ['dungeon', 'cell', 'prison', 'tomb', 'crypt', 'ancient']):
        return ART_DUNGEON
    return ART_CAVE  # generic fallback


def get_npc_art(name: str) -> str:
    """Returns ASCII art for an NPC based on its name keywords."""
    n = name.lower()
    if any(k in n for k in ['goblin', 'orc', 'troll', 'imp', 'gnoll']):
        return ART_GOBLIN
    if any(k in n for k in ['skeleton', 'bone', 'undead', 'lich']):
        return ART_SKELETON
    if any(k in n for k in ['ghost', 'spirit', 'wraith', 'specter', 'phantom']):
        return ART_GHOST
    if any(k in n for k in ['witch', 'hag', 'crone']):
        return ART_WITCH
    if any(k in n for k in ['wizard', 'mage', 'sorcerer', 'warlock', 'enchanter']):
        return ART_WIZARD
    if any(k in n for k in ['knight', 'guard', 'soldier', 'warrior', 'paladin']):
        return ART_KNIGHT
    if any(k in n for k in ['bandit', 'thief', 'rogue', 'assassin', 'brigand']):
        return ART_BANDIT
    if any(k in n for k in ['dragon', 'wyvern', 'drake', 'wyrm']):
        return ART_DRAGON
    return ART_GENERIC_MONSTER  # fallback


def get_item_art(name: str) -> str:
    """Returns ASCII art for an item based on its name keywords."""
    n = name.lower()
    if any(k in n for k in ['dagger', 'knife', 'stiletto']):
        return ART_DAGGER
    if any(k in n for k in ['sword', 'blade', 'axe', 'scimitar', 'sabre', 'claymore']):
        return ART_SWORD
    if any(k in n for k in ['shield', 'buckler', 'targe']):
        return ART_SHIELD
    if any(k in n for k in ['armor', 'armour', 'gauntlet', 'breastplate', 'mail']):
        return ART_ARMOR
    if any(k in n for k in ['helm', 'helmet', 'cap', 'crown']):
        return ART_CROWN
    if any(k in n for k in ['potion', 'elixir', 'flask', 'draught', 'brew', 'vial']):
        return ART_POTION
    if any(k in n for k in ['scroll', 'tome', 'spellbook', 'grimoire']):
        return ART_SCROLL
    if any(k in n for k in ['book', 'journal', 'diary', 'parchment']):
        return ART_SCROLL
    if any(k in n for k in ['wand', 'staff', 'rod', 'orb', 'scepter', 'crystal']):
        return ART_WAND
    if any(k in n for k in ['key', 'lockpick']):
        return ART_KEY
    if any(k in n for k in ['apple', 'food', 'bread', 'meat', 'fruit', 'herb']):
        return ART_APPLE
    return ART_ITEM  # generic fallback
